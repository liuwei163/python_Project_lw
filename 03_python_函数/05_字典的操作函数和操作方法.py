# 操作函数                    描述
# len(d)                    字典d的元素个数(长度)
# min(d)                    字典d中键的最小值(键之间具有可比性)
# max(d)                    字典d中键的最大值(键之间具有可比性)
# dict()                    生成一个空字典

d = {2: "刘伟", 10: "张三"}
print(len(d))  # 一个键值对为一个元素
print(min(d))
print(max(d))

lw = dict()
print(lw)