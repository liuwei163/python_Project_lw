################################for循环#######################################
# 从数据集合中逐一提取元素，放在循环变量中，对于每个提取的元素执行一次语句块。
# for语句的循环执行次数是根据集合中元素个数确定的。
# 数据集合可以是字符串、文件、range()函数或其他组合数据类型等
"""
  语法格式:
        for 变量名 in  数据集合:
            <语句>
"""
# 对于字符串，就是遍历每一个字符
for i in "python":
    print(i)

# 对于列表，就是遍历每一个元素
lt = [1, 3, 6, 9, "刘伟"]
for i in lt:
    print(i)

# 对元组，也是遍历每一个元素
t = (1, 2, 3, 10, 20)
for i in t:
    print(i)
    print(t)  # 打印变量t，打印了5次，因为遍历了5个元素

# 对字典，进行遍历，会拿出字典中的 键  而没有字典中的值
d = {"name": "刘伟", "age": 19}
for i in d:
    print(i)


# for 和 range() 函数使用
for i in range(5):   # range()函数是从0 开始
    print(i)

