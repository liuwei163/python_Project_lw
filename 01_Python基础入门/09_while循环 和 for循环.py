# for循环的扩展模式
# 当for循环正常执行之后，程序会继续执行else语句中内容，else语句只在循环正常执行之后才执行并结束。

"""
for 变量名 in 集合:
    <语句块1>
else:
    <语句块2>
"""
for i in range(1, 6):
    if i == 3:
        print("第三个有毒")
        break
    print("第{}个鸡腿真香".format(i))

else:
    print("NICE")

# while循环的扩展模式
# 当while循环正常执行之后，程序会继续执行else语句中内容，else语句只在循环正常执行之后才执行并结束。
number = 1
while number <= 10:
    print(number, "abc")  # 运行循环的内容
    # 循环一次以后给number变量加1(改变循环变量)
    number = number + 1
else:
    print("循环正常结束")
